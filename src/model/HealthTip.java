package model;


public class HealthTip extends CheerUser {
	
	private HealthTip left;
	
	private HealthTip right;
	
	public HealthTip() {
	}

	public HealthTip getLeft() {
		return left;
	}

	public void setLeft(HealthTip left) {
		this.left = left;
	}

	public HealthTip getRight() {
		return right;
	}

	public void setRight(HealthTip right) {
		this.right = right;
	}

	
}
